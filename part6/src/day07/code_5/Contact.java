package day07.code_5;

public class Contact {

    //姓名
    private String name;

    //电话号码
    private String phone;

    public Contact(String name, String phone) {
        this.name = name;
        this.phone = phone;
    }

    public String getName() {
        return name;
    }

    public String getPhone() {
        return phone;
    }


}
